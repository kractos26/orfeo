------------------------------
--dia_sig_ant(): Recibe como parametros la fecha de inicio y la palabra Proximo  Anterior, y devolver el proximo o anterior dia habil.
-------------------------------------------

CREATE FUNCTION dia_sig_ant(@fechaini DATETIME, @proxant VARCHAR(10))
RETURNS DATETIME
AS
BEGIN
    DECLARE @intDay INT
    DECLARE @rtResult DATETIME
    SET @intDay = DATEPART(weekday,@fechaini)
    IF @proxant = 'Previous'
        IF @intDay = 1
            SET @rtResult = DATEADD(dd,-3,@fechaini)
        ELSE
            SET @rtResult = DATEADD(dd,-1,@fechaini)
    ELSE IF @proxant = 'Next'
        IF @intDay = 5
            SET @rtResult = DATEADD(dd,3,@fechaini)
        ELSE
            SET @rtResult = DATEADD(dd,1,@fechaini)
    ELSE
            SET @rtResult = @fechaini
    RETURN @rtResult
END

----------------------------------------------------
--sumadiashabiles(): Esta funcion recibe como parametros la fecha de inicio y el numero de dias habiles
--y retornara la fecha correspondiente teniendo en cuanta los dias festivos entre semana.
----------------------------------------------------

CREATE FUNCTION sumadiashabiles(@fecinicio datetime, @numdias integer)
RETURNS datetime
AS
BEGIN
    DECLARE @NextDay datetime
    DECLARE @Counter int
    DECLARE @diasem INT
    DECLARE @diasNH INT
    IF @numdias>0
    BEGIN
        SET @Counter=1
        SET @NextDay=CONVERT(datetime ,(CONVERT(char(10),@fecinicio,101)),101 )
        WHILE @Counter <= @numdias
        BEGIN
            SET @NextDay =dbo.dia_sig_ant(@NextDay,'Next' )
            SET @Counter=@Counter+1
        END
    END
    ELSE
        BEGIN
        SET @Counter=ABS(@numdias)
        SET @NextDay=CONVERT(datetime ,(CONVERT(char(10),@fecinicio,101)),101 )

        WHILE @Counter>0
        BEGIN
            SET @NextDay = dbo.dia_sig_ant(@NextDay,'Previous')
            SET @Counter=@Counter-1
        END
    END
    select @diasNH=    (select count(noh_fecha) from sgd_noh_nohabiles where noh_fecha between @fecinicio and @NextDay)
    set @NextDay=dateadd(dd,@diasNH,@NextDay)
    select @diasNH=    (select count(noh_fecha) from sgd_noh_nohabiles where noh_fecha = @NextDay)
    set @NextDay=dateadd(dd,@diasNH,@NextDay)
    SET @diasem = DATEPART(weekday,@NextDay)
    IF @diasem=6 or @diasem=7 begin
        SET @NextDay=dateadd(dd,2,@NextDay)
    END
    RETURN @NextDay
END

----------------------------------------------------------
--diashabiles():Funcion que recibe como parametros fecha inicial y fecha final
--y retorna dias habiles que han transcurrido entre dichas fechas teniendo en cuenta los festivos.
----------------------------------------------------------


CREATE FUNCTION diashabiles(@fecinicio datetime, @fecfin datetime)
RETURNS INT
AS
BEGIN
    DECLARE @fechaAnterior datetime
    DECLARE @dias INT
    DECLARE @diasNH INT
    SET @dias=0
    SET @fechaAnterior=@fecinicio
    SELECT @diasNH = (select count(noh_fecha) from sgd_noh_nohabiles where noh_fecha between @fecinicio and @fecfin)
    IF @fechaAnterior<=@fecfin
    BEGIN
        SET @fechaAnterior =dbo.dia_sig_ant(@fechaAnterior,'Next' )
        WHILE @fechaAnterior<=@fecfin
        BEGIN
            SET @fechaAnterior =dbo.dia_sig_ant(@fechaAnterior,'Next' )
            SET @dias=@dias-1
        END
        SET @dias=@dias+@diasNH
    END
    ELSE BEGIN
        SET @fechaAnterior=@fecinicio
        WHILE @fechaAnterior>@fecfin
        BEGIN
            SET @fechaAnterior = dbo.dia_sig_ant(@fechaAnterior,'Previous')
            SET @dias=@dias+1
        END
        SET @dias=@dias-@diasNH
    END
    RETURN @dias
END


